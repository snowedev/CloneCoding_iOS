import Foundation

import UIKit
extension UIColor {

  @nonobjc class var brownGrey: UIColor {
    return UIColor(red: 111.0 / 255.0, green: 111.0 / 255.0, blue: 111.0 / 255.0, alpha: 1.0)
  }
    
  @nonobjc class var brightDarkGrey: UIColor {
      return UIColor(red: 210.0 / 255.0, green: 210.0 / 255.0, blue: 210.0 / 255.0, alpha: 1.0)
  }

}
